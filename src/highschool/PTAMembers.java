    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if (name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Input A valid Name");
        } else {
            if (DataValidation.phoneValidator(phone.getText())) {
                try {
                    ps = con.prepareStatement("Insert Into pta_table values('" + id.getText() + "','" + name.getText() + "','" + phone.getText() + "','" + "" + "')");
                    ps.execute();
                    JOptionPane.showMessageDialog(this, "PTA Member Saved Successfully");
                    phone.setText("");
                    id.setText("");
                    name.setText("");
                    id.setText(IdGenerator.keyGen());


                } catch (Exception sq) {
                    sq.printStackTrace();
                }


            } else {
                JOptionPane.showMessageDialog(this, "Input A valid Phone Number");
            }
        }
    }//GEN-LAST:event_saveActionPerformed

