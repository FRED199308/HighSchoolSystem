    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        if (tab.getSelectedRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "Kindly Select The PTA Memmber To Delete");

        } else {

            try {

                String ref = model.getValueAt(tab.getSelectedRow(), 3).toString();
                String sql = "Delete from pta_table where id='" + ref + "'";
                ps = con.prepareStatement(sql);
                ps.execute();
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully");
                model.removeRow(tab.getSelectedRow());

            } catch (Exception sq) {
                sq.printStackTrace();
            }

        }
    }//GEN-LAST:event_deleteActionPerformed

